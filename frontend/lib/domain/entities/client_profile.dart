class ClientProfile {
  final String name;
  final String bio;
  final String gender;
  final String birthDate;
  final String email;
  final String phone;
  final String imageUrl;

  ClientProfile({
    required this.name,
    required this.bio,
    required this.gender,
    required this.birthDate,
    required this.email,
    required this.phone,
    required this.imageUrl,
  });
}
